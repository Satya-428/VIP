// Box pattern
import java.util.Scanner;
public class Patternone {
	    public static void main(String[] args) {
	    int i,j,n,m;
	    Scanner s=new Scanner(System.in);
	    n=s.nextInt();
	    for(i=1;i<=n;i++){
	        for(j=0;j<=n;j++){
	            System.out.print("*"+" ");
	        }
	        System.out.println();
	    }
	    
	        
	    }
	}


